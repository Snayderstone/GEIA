    <section id="mission-vision">
      <h2>Misión</h2>
      <p>El proyecto GEIA tiene como misión principal desarrollar una plataforma para estimar los daños económicos en diferentes sectores de la economía ecuatoriana causados por eventos específicos, con el fin de identificar áreas vulnerables y proporcionar información útil para la toma de decisiones estratégicas.</p>
      
      <h2>Visión</h2>
      <p>Nuestra visión es crear una herramienta innovadora que permita a las autoridades gubernamentales, empresas y otros actores clave anticipar y mitigar los impactos económicos de eventos adversos, fortaleciendo así la resiliencia económica del Ecuador.</p>
    </section>
  
    <section id="objectives">
      <h2>Objetivos del Proyecto</h2>
      <h3>Objetivo General</h3>
      <p>Implementar un modelo predictivo para estimar los daños económicos en diferentes sectores de Ecuador causados por eventos adversos, con el fin de identificar áreas vulnerables y mejorar la capacidad de respuesta ante situaciones que puedan afectar la economía del país.</p>
  
      <h3>Objetivos Específicos</h3>
      <ul>
        <li>Realizar un análisis de datos y minería de datos para descubrir patrones entre eventos y la baja de la economía en diferentes áreas de Ecuador.</li>
        <li>Identificar áreas vulnerables y estimar el impacto económico potencial de eventos específicos en sectores clave de la economía ecuatoriana.</li>
        <li>Implementar inteligencia artificial generativa para complementar el modelo predictivo.</li>
      </ul>
    </section>
  
